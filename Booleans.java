// Booleans Overview
public class Booleans {
	public static void main(String[]args) {
		// Declares boolean values
		boolean trueStatement = true;
		boolean falseStatement = false;
		// Prints booleans as true or false
		System.out.println(trueStatement);		// Outputs true
		System.out.println(falseStatement);		// Outputs false
		// Declares int values
		int x = 10;
		int y = 9;
		int z = 20;
		// Math + Comparisons
		System.out.println(x > y); 		// returns true, b/c 10 is higher than 9
		System.out.println(10 > 9); 	// returns true, b/c 10 is higher than 9
		System.out.println(z == 20); 	// returns true, b/c the value of z is equal to 20
		System.out.println(10 == 15); 	// returns false, b/c 10 is not equal to 15
	/*
	Sources
		https://www.w3schools.com/java/java_booleans.asp
	*/
	}
}